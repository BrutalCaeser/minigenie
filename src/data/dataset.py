"""
PyTorch Dataset for world model training.

Loads episodes from .npz files (generated by generate_procgen.py) and returns
(context_frames, action, target_frame) tuples for training.

Each sample consists of:
  - context_frames: [H*3, h, w] float32 in [0, 1] — H context frames channel-stacked
  - action: scalar int64 — the action taken at the current timestep
  - target_frame: [3, h, w] float32 in [0, 1] — the frame to predict

Critical: All file-globbing code filters out macOS ._* Apple Double metadata files
that appear on external SSDs.
"""

import os
from glob import glob
from typing import Callable, List, Optional, Tuple

import numpy as np
import torch
import torch.nn.functional as F
from torch.utils.data import Dataset


class WorldModelDataset(Dataset):
    """Returns (context_frames, action, target_frame) tuples for training.

    Loads Procgen episodes from .npz files. Each episode file contains:
      - 'frames': uint8 array [T, H, W, 3]
      - 'actions': int32 array [T-1]

    Builds a flat index over all valid (episode, timestep) pairs where
    enough context frames exist before the timestep.

    Args:
        data_dir: Path to directory containing an 'episodes/' subfolder with .npz files.
                  Can also point directly to a folder of .npz files.
        context_length: Number of past frames to stack as context (default: 4).
        frame_skip: Spacing between context frames (default: 1).
        target_resolution: If set, resize frames to this (H, W) using bilinear interpolation.
                          E.g., (128, 128) to upscale from Procgen's native 64×64.
        max_episodes: Maximum number of episodes to load (None = all). Useful for
                      memory-constrained local testing.
        transform: Optional callable applied to each sample dict before returning.
        validate: If True, run data integrity checks on load (default: True).
    """

    def __init__(
        self,
        data_dir: str,
        context_length: int = 4,
        frame_skip: int = 1,
        target_resolution: Optional[Tuple[int, int]] = None,
        max_episodes: Optional[int] = None,
        transform: Optional[Callable] = None,
        validate: bool = True,
    ) -> None:
        self.context_length = context_length
        self.frame_skip = frame_skip
        self.target_resolution = target_resolution
        self.transform = transform

        # Find .npz files — check both data_dir directly and data_dir/episodes/
        episodes_dir = os.path.join(data_dir, "episodes")
        if os.path.isdir(episodes_dir):
            search_dir = episodes_dir
        else:
            search_dir = data_dir

        # Filter out macOS ._* Apple Double metadata files (critical on external SSD)
        all_paths = sorted(glob(os.path.join(search_dir, "*.npz")))
        npz_paths = [p for p in all_paths if not os.path.basename(p).startswith("._")]

        if len(npz_paths) == 0:
            raise FileNotFoundError(
                f"No .npz episode files found in {search_dir}. "
                f"Run generate_procgen.py first to create episode data."
            )

        # Optionally limit number of episodes (for memory-constrained testing)
        if max_episodes is not None:
            npz_paths = npz_paths[:max_episodes]

        # Load all episodes into memory
        self.episodes: List[Tuple[np.ndarray, np.ndarray]] = []
        for path in npz_paths:
            data = np.load(path)
            frames = data["frames"]   # [T, H, W, 3] uint8
            actions = data["actions"]  # [T-1] int32
            self.episodes.append((frames, actions))

        # Validate data integrity
        if validate:
            self._validate()

        # Build flat index: (episode_idx, frame_idx_t) for all valid timesteps
        # A timestep t is valid if:
        #   - There are enough prior frames for context: t >= context_length * frame_skip
        #   - There is a next frame to predict: t < T - 1
        #   - There is an action at time t: t < len(actions)
        self.index: List[Tuple[int, int]] = []
        for ep_idx, (frames, actions) in enumerate(self.episodes):
            T = len(frames)
            min_t = context_length * frame_skip
            # t ranges from min_t to T-2 (inclusive) — at t we use action[t] and predict frames[t+1]
            for t in range(min_t, min(T - 1, len(actions))):
                self.index.append((ep_idx, t))

    def _validate(self) -> None:
        """Run data integrity checks on loaded episodes."""
        for ep_idx, (frames, actions) in enumerate(self.episodes):
            # Check shapes
            if frames.ndim != 4 or frames.shape[-1] != 3:
                raise ValueError(
                    f"Episode {ep_idx}: expected frames shape [T, H, W, 3], "
                    f"got {frames.shape}"
                )
            if actions.ndim != 1:
                raise ValueError(
                    f"Episode {ep_idx}: expected actions shape [T-1], got {actions.shape}"
                )
            if len(actions) != len(frames) - 1:
                raise ValueError(
                    f"Episode {ep_idx}: frames has {len(frames)} entries but "
                    f"actions has {len(actions)} (expected {len(frames) - 1})"
                )
            # Check dtypes
            if frames.dtype != np.uint8:
                raise ValueError(
                    f"Episode {ep_idx}: expected uint8 frames, got {frames.dtype}"
                )
            # Check value ranges
            if np.any(actions < 0) or np.any(actions >= 15):
                raise ValueError(
                    f"Episode {ep_idx}: actions outside valid range [0, 14]"
                )
            # Check for NaN (shouldn't happen with uint8 but check anyway)
            if np.any(np.isnan(frames.astype(np.float32))):
                raise ValueError(f"Episode {ep_idx}: frames contain NaN values")

    def __len__(self) -> int:
        return len(self.index)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        """Get a single training sample.

        Args:
            idx: Sample index.

        Returns:
            Tuple of (context, action, target):
              - context: [H*3, h, w] float32 in [0, 1] — H context frames channel-stacked
              - action: scalar int64 — the action taken at timestep t
              - target: [3, h, w] float32 in [0, 1] — the next frame (frames[t+1])
        """
        ep_idx, t = self.index[idx]
        frames, actions = self.episodes[ep_idx]

        # Context: H frames before time t, spaced by frame_skip
        # If context_length=4, frame_skip=1: indices are [t-4, t-3, t-2, t-1]
        context_indices = [
            t - i * self.frame_skip
            for i in range(self.context_length, 0, -1)
        ]
        context = np.stack([frames[i] for i in context_indices])  # [H, h, w, 3]

        target = frames[t + 1]  # [h, w, 3]
        action = actions[t]     # scalar int

        # Convert to tensors: normalize to [0, 1] float32, rearrange to channels-first
        context = torch.from_numpy(context.copy()).float().div(255.0)  # [H, h, w, 3]
        context = context.permute(0, 3, 1, 2)  # [H, 3, h, w]
        context = context.reshape(-1, *context.shape[2:])  # [H*3, h, w]

        target = torch.from_numpy(target.copy()).float().div(255.0)  # [h, w, 3]
        target = target.permute(2, 0, 1)  # [3, h, w]

        action = torch.tensor(action, dtype=torch.long)

        # Optionally resize to target resolution (e.g., 64×64 → 128×128)
        if self.target_resolution is not None:
            th, tw = self.target_resolution
            # Resize context: [H*3, h, w] → [H*3, th, tw]
            context = F.interpolate(
                context.unsqueeze(0), size=(th, tw), mode="bilinear", align_corners=False
            ).squeeze(0)
            # Resize target: [3, h, w] → [3, th, tw]
            target = F.interpolate(
                target.unsqueeze(0), size=(th, tw), mode="bilinear", align_corners=False
            ).squeeze(0)

        # Apply optional transform
        if self.transform is not None:
            sample = {"context": context, "action": action, "target": target}
            sample = self.transform(sample)
            context, action, target = sample["context"], sample["action"], sample["target"]

        return context, action, target

    def __repr__(self) -> str:
        return (
            f"WorldModelDataset("
            f"episodes={len(self.episodes)}, "
            f"samples={len(self.index)}, "
            f"context_length={self.context_length}, "
            f"frame_skip={self.frame_skip}, "
            f"resolution={self.target_resolution})"
        )

    @property
    def frame_shape(self) -> Tuple[int, ...]:
        """Shape of a single frame (H, W, 3) from the loaded data."""
        return self.episodes[0][0].shape[1:]  # [H, W, 3]

    @property
    def num_episodes(self) -> int:
        """Number of loaded episodes."""
        return len(self.episodes)
